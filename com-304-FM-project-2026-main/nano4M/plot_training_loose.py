def smooth_values(values, window=31):
    """
    Lissage par moyenne glissante.
    Contrairement à np.convolve(..., mode="same") simple,
    on pad les bords pour éviter une chute artificielle au début/à la fin.
    """
    values = np.asarray(values, dtype=float)

    if len(values) == 0:
        return values

    if window <= 1 or len(values) < window:
        return values

    if window % 2 == 0:
        window += 1

    left = window // 2
    right = window // 2

    padded = np.pad(values, (left, right), mode="edge")
    kernel = np.ones(window, dtype=float) / window
    return np.convolve(padded, kernel, mode="valid")


def clean_model_data(data, model_name):
    """
    Vérifie les données et renvoie deux tableaux numpy : steps, losses.
    Les modèles vides sont ignorés automatiquement.
    Les steps dupliqués sont dédoublonnés en gardant la dernière valeur.
    """
    if not data:
        print(f"[INFO] {model_name} est vide : ignoré.")
        return None, None

    dedup = {}

    for point in data:
        if len(point) != 2:
            raise ValueError(
                f"Erreur dans {model_name} : chaque point doit être au format (step, loss)."
            )

        step, loss = point
        dedup[int(step)] = float(loss)

    sorted_data = sorted(dedup.items())
    steps = np.array([p[0] for p in sorted_data], dtype=float)
    losses = np.array([p[1] for p in sorted_data], dtype=float)

    return steps, losses



